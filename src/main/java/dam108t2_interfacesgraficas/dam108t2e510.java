    public dam108t2e510() {
        initComponents();
        this.setSize(500, 500);
        // creamos el tablero de botones con dos bucles
        tablero = new JButton [8][8];
        
        for (int f = 0; f < tablero.length; f++) {
            for (int c = 0; c < tablero[f].length; c++) {
                tablero [f][c] = new JButton(); //inicializamos el array del tablero
                
                //ponemos medidas a los botones
                tablero [f][c].setBounds(c*50, f*50, 54, 54);
                
                // establecemos un colores alternos a las casillas para simular un tablero de ajedrez
                if (f % 2 == 0 && c % 2 == 0) tablero[f][c].setBackground(Color.black);
                if (f % 2 != 0 && c % 2 != 0) tablero[f][c].setBackground(Color.black);

                
                
                //creamos un evento para cada boton
                tablero [f][c].addActionListener(new java.awt.event.ActionListener(){
                    @Override
                    public void actionPerformed(java.awt.event.ActionEvent evt) {
                       tareaSiPulsa(evt);
                    }
                });
                // aÃ±adimos los botones al panel
                this.add(tablero[f][c]);
                
                //establecemos un nombre para boton con el nombre de la fila y de la columna
                tablero [f][c].setName(Integer.toString(f+1)+Integer.toString(c+1));
            }
        }
    }
        
    public void tareaSiPulsa(java.awt.event.ActionEvent e){
        String nombre = ((JButton)e.getSource()).getName();
        JOptionPane.showMessageDialog(this,"has pulsado la casilla "+nombre);
    }

